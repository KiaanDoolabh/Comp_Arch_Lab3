    .section .text
    .globl _start

_start:
    ############################################################
    # Setup
    ############################################################
    lui     x1, 0x10000        # base address in x1
    addi    x2, x0, 10         # x2 = 10
    addi    x3, x0, 5          # x3 = 5

    ############################################################
    # Arithmetic / Logical
    ############################################################
    add     x4, x2, x3         # x4 = 15
    sub     x5, x2, x3         # x5 = 5

    and     x6, x2, x3
    andi    x7, x2, 3

    or      x8, x2, x3
    ori     x9, x2, 1

    xor     x10, x2, x3
    xori    x11, x2, 7

    ############################################################
    # Shifts
    ############################################################
    sll     x12, x2, x3
    slli    x13, x2, 1

    srl     x14, x2, x3
    srli    x15, x2, 1

    sra     x16, x2, x3
    srai    x17, x2, 1

    ############################################################
    # Set Less Than
    ############################################################
    slt     x18, x3, x2
    sltu    x19, x3, x2

    slti    x20, x3, 10
    sltiu   x21, x3, 10

    ############################################################
    # Memory (store)
    ############################################################
    sw      x2, 0(x1)
    sh      x3, 4(x1)
    sb      x3, 6(x1)

    ############################################################
    # Memory (load)
    ############################################################
    lw      x22, 0(x1)
    lh      x23, 4(x1)
    lhu     x24, 4(x1)
    lb      x25, 6(x1)
    lbu     x26, 6(x1)

    ############################################################
    # Branches
    ############################################################
    beq     x2, x2, label_beq
    addi    x27, x0, 1         # should be skipped

label_beq:
    bne     x2, x3, label_bne
    addi    x27, x0, 2

label_bne:
    blt     x3, x2, label_blt
    addi    x27, x0, 3

label_blt:
    bge     x2, x3, label_bge
    addi    x27, x0, 4

label_bge:
    bltu    x3, x2, label_bltu
    addi    x27, x0, 5

label_bltu:
    bgeu    x2, x3, label_bgeu
    addi    x27, x0, 6

label_bgeu:

    ############################################################
    # Jumps
    ############################################################
    auipc   x28, 0             # PC-relative test

    jal     x29, jump_target

    addi    x30, x0, 123       # should be skipped

jump_target:
    addi    x30, x0, 42

    jalr    x31, 0(x29)        # jump back to return addr (infinite loop)

    ############################################################
    # End (loop forever)
    ############################################################
end:
    beq     x0, x0, end